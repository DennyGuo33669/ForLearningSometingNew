package com.domain;

public class Student {
	

	private String stuNo=null;
	private String classNo=null;
	private String className=null;
	private double creditHour;
	private double regularScore;
	private double experimentalScore;
	private double examinationScore;
	private double comprehensiveScore;
	private double realCredit;
	public String getStuNo() {
		return stuNo;
	}
	public void setStuNo(String stuNo) {
		this.stuNo = stuNo;
	}
	public String getClassNo() {
		return classNo;
	}
	public void setClassNo(String classNo) {
		this.classNo = classNo;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public double getCreditHour() {
		return creditHour;
	}
	public void setCreditHour(double creditHour) {
		this.creditHour = creditHour;
	}
	public double getRregularScore() {
		return regularScore;
	}
	public void setRregularScore(double regularScore) {
		this.regularScore = regularScore;
	}
	public double getExperimentalScore() {
		return experimentalScore;
	}
	public void setExperimentalScore(double experimentalScore) {
		this.experimentalScore = experimentalScore;
	}
	public double getExaminationScore() {
		return examinationScore;
	}
	public void setExaminationScore(double examinationScore) {
		this.examinationScore = examinationScore;
	}
	public double getComprehensiveScore() {
		return comprehensiveScore;
	}
	public void setComprehensiveScore(double comprehensiveScore) {
		this.comprehensiveScore = comprehensiveScore;
	}
	public double getRealcredit() {
		return realCredit;
	}
	public void setRealcredit(double realCredit) {
		this.realCredit = realCredit;
	}
}
